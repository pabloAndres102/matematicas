#   Suponga que usted va a comprar una casa que en metros cuadrados mide 464 mts². El valor en metros cuadrados vale COP 850.000
#   ¿Cuál es el valor de la casa en pesos colombianos?
#   ¿Cuál es el valor de la casa en dólares colombianos sabiendo que el precio del dólar al 02 de junio de 2022 es de: COP 3779


medidaCasa = 464 

valorMetro = 850000

casaPesos = medidaCasa * valorMetro

casaDolares = valorMetro * 3779

print(f'El valor de la casa en pesos colombianos: {casaPesos}')

print(f'El valor de la casa en dolares es {casaDolares}')