##   En el área del piso de una habitación, se pueden pegar 70 baldosas cuadradas que tienen la misma longitud en su lado.
##   La longitud del lado de cada baldosa es de 20 cms. ¿Cuál es el área total del piso de la habitación?

areaBaldosa = 20 ** 2

areaPiso = areaBaldosa * 70


print(f'El area de la habitacion : {areaPiso}')


